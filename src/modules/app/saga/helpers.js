export const getMonthName = monthNumber => {
    const monthArray = [
        'января',
        'февраля',
        'марта',
        'апреля',
        'мая',
        'июня',
        'июля',
        'августа',
        'сентября',
        'октября',
        'ноября',
        'декабря',
    ];

    return monthArray[monthNumber]; 
}; 